// Switch e Case

let airport = 'VCP'

switch (airport){
    case'GRU':
        console.log('Guarulhos')
        break
    case'CGH':
        console.log('Congonhas')
        break
    case 'VCP':
        console.log('Viracopos')
        break
    default:
        console.log('Unknown')
    break

}






